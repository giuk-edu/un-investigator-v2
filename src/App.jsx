export default function App() {
  const [stage, setStage] = useState("welcome");
  const [name, setName] = useState("");
  const [selectedConflict, setSelectedConflict] = useState(null);
  const [selectedPersona, setSelectedPersona] = useState(null);
  const [transcript, setTranscript] = useState([]);
  const [report, setReport] = useState(null);
  const [interviewMessages, setInterviewMessages] = useState(null);
  const [interviewChecked, setInterviewChecked] = useState(null);

  // 분쟁 ID에 따라 페르소나 목록 가져오기
  const getPersonasForConflict = (conflictId) => {
    if (conflictId === "sudan") return SUDAN_PERSONAS;
    if (conflictId === "ukraine") return UKRAINE_PERSONAS;
    if (conflictId === "myanmar") return MYANMAR_PERSONAS;
    if (conflictId === "scs") return SCS_PERSONAS;
    return [];
  };

  return (
    <div style={{
      minHeight: "100vh",
      background: "#0a1628",
      color: "#f4ead5",
      fontFamily: '"Cormorant Garamond", "Noto Serif KR", serif',
      position: "relative",
      overflow: "hidden",
    }}>
      <div style={{
        position: "absolute", inset: 0,
        backgroundImage: `repeating-linear-gradient(0deg, rgba(212,175,55,0.03) 0px, rgba(212,175,55,0.03) 1px, transparent 1px, transparent 4px)`,
        pointerEvents: "none",
      }} />
      <Header />

      {stage === "welcome" && (
        <Stage1Welcome
          name={name}
          setName={setName}
          onNext={() => name.trim().length >= 2 && setStage("verified")}
        />
      )}
      {stage === "verified" && (
        <Stage2Verified name={name} onNext={() => setStage("selectConflict")} />
      )}
      {stage === "selectConflict" && (
        <Stage3SelectConflict
          name={name}
          onSelect={(c) => {
            // 다른 분쟁 선택 시 인터뷰 상태 초기화
            if (selectedConflict && selectedConflict.id !== c.id) {
              setSelectedPersona(null);
              setInterviewMessages(null);
              setInterviewChecked(null);
              setTranscript([]);
              setReport(null);
            }
            setSelectedConflict(c);
            setStage("conflictDetail");
          }}
          onBack={() => setStage("verified")}
        />
      )}
      {stage === "conflictDetail" && selectedConflict && (
        <Stage4ConflictDetail
          conflict={selectedConflict}
          onBack={() => setStage("selectConflict")}
          onConfirm={(c) => setStage("selectPersona")}
        />
      )}
      {stage === "selectPersona" && selectedConflict && (
        <Stage5SelectPersona
          name={name}
          conflict={selectedConflict}
          personas={getPersonasForConflict(selectedConflict.id)}
          onSelect={(p) => {
            // 다른 페르소나 선택 시 인터뷰 상태 초기화
            if (selectedPersona && selectedPersona.id !== p.id) {
              setInterviewMessages(null);
              setInterviewChecked(null);
              setTranscript([]);
              setReport(null);
            }
            setSelectedPersona(p);
            setStage("personaConfirm");
          }}
          onBack={() => setStage("conflictDetail")}
        />
      )}
      {stage === "personaConfirm" && selectedConflict && selectedPersona && (
        <Stage6PersonaConfirm
          name={name}
          conflict={selectedConflict}
          persona={selectedPersona}
          onConfirm={(p) => setStage("mandate")}
          onBack={() => setStage("selectPersona")}
        />
      )}
      {stage === "mandate" && selectedConflict && selectedPersona && (
        <Stage7Mandate
          name={name}
          conflict={selectedConflict}
          persona={selectedPersona}
          onProceed={() => setStage("interview")}
          onBack={() => setStage("personaConfirm")}
        />
      )}
      {stage === "interview" && selectedConflict && selectedPersona && (
        <Stage8Interview
          name={name}
          conflict={selectedConflict}
          persona={selectedPersona}
          savedMessages={interviewMessages}
          savedChecked={interviewChecked}
          onMessagesChange={setInterviewMessages}
          onCheckedChange={setInterviewChecked}
          onComplete={(t) => {
            setTranscript(t);
            setStage("report");
          }}
          onBack={() => setStage("mandate")}
        />
      )}
      {stage === "report" && selectedConflict && selectedPersona && (
        <Stage9Report
          name={name}
          conflict={selectedConflict}
          persona={selectedPersona}
          transcript={transcript}
          onComplete={(r) => {
            setReport(r);
            setStage("reportComplete");
          }}
          onBack={() => setStage("interview")}
        />
      )}
      {stage === "reportComplete" && selectedConflict && selectedPersona && report && (
        <Stage10ReportComplete
          name={name}
          conflict={selectedConflict}
          persona={selectedPersona}
          report={report}
          onBack={() => setStage("report")}
          onRestart={() => {
            setStage("welcome");
            setName("");
            setSelectedConflict(null);
            setSelectedPersona(null);
            setTranscript([]);
            setReport(null);
          }}
        />
      )}
      {stage === "nextPlaceholder" && (
        <StagePlaceholder
          title="NEXT STEP · 다음 단계"
          message="갤러리 워크 (다음에 제작)"
          onBack={() => setStage("reportComplete")}
        />
      )}

      <style>{`
        @keyframes fadeIn {
          from { opacity: 0; transform: translateY(10px); }
          to { opacity: 1; transform: translateY(0); }
        }
        @keyframes fadeInMsg {
          from { opacity: 0; transform: translateY(8px); }
          to { opacity: 1; transform: translateY(0); }
        }
        @keyframes pulse {
          0%, 60%, 100% { opacity: 0.3; transform: scale(1); }
          30% { opacity: 1; transform: scale(1.3); }
        }
        input::placeholder { color: rgba(244,234,213,0.3); font-style: italic; }
        textarea::placeholder { color: rgba(244,234,213,0.3); font-style: italic; }
        input:focus { border-bottom-color: #d4af37 !important; }
        textarea:focus { border-color: rgba(212,175,55,0.5) !important; }
        @media (max-width: 768px) {
          h1 { font-size: 36px !important; }
        }
      `}</style>
    </div>
  );
}
